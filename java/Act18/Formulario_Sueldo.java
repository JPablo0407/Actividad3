    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        // TODO add your handling code here:
        double horas, VHoras,retencion;
        
            
        horas=Double.parseDouble(txthoras.getText());
        VHoras=Double.parseDouble(txtvalor.getText());
        retencion=Double.parseDouble(txtretencion.getText());
        
        txtbruto.setText(String.valueOf(Sueldo.salario_bruto(horas, VHoras)));
        txtneto.setText(String.valueOf(Sueldo.salario_neto(horas, VHoras, retencion)));

        

    }//GEN-LAST:event_btncalcularActionPerformed

